    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");

        }catch(Exception e){
            System.out.println(e);
        }

        final String ID = "tshaw7";
        final String PW = "COSC*dw7o7";
        final String SERVER = "jdbc:mysql://triton.towson.edu:3360/?serverTimezone=EST#/tshaw7db?useSSL=false";

        try {
            Connection con = DriverManager.getConnection(SERVER, ID, PW);
            

            String sql = "INSERT into tshaw7db.StaffMember values (?,?,?,?,?,?,?,?,?,?)";
            PreparedStatement pstmt = con.prepareStatement(sql);
            //Inserting a new Staff Member into the database
            //Sets up the StaffID
            pstmt.setString(1, SID.getText());
            //Sets up the SSN 
            pstmt.setString(2, SSN.getText());
            //Sets up the Phone Number
            pstmt.setString(3, PhoneNo.getText());
            //Sets up the First Name
            pstmt.setString(4, Fname.getText());
            //Sets up the Last Name
            pstmt.setString(5, Lname.getText());
            //Sets up the Sex
            String sex = Sex.getSelectedItem().toString();
            pstmt.setString(6, sex);
            //Sets up the Salary
            pstmt.setString(7, Salary.getText());
            //Sets up the DOB
            pstmt.setString(8, DOB.getText());
            //Sets up the Role
            pstmt.setString(9, Role.getText());
            //Sets up the Address
            pstmt.setString(10, Address.getText());
            
            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Insertion Successful");
            DefaultTableModel model = (DefaultTableModel)Staff.getModel();
            model.setRowCount(0);
            show_staff();
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_addButtonActionPerformed

    private void StaffMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_StaffMouseClicked
        int i = Staff.getSelectedRow();
        TableModel model = Staff.getModel();
        SID.setText(model.getValueAt(i, 0).toString());
        SSN.setText(model.getValueAt(i, 1).toString());
        PhoneNo.setText(model.getValueAt(i, 2).toString());
        Fname.setText(model.getValueAt(i, 3).toString());
        Lname.setText(model.getValueAt(i, 4).toString());
        String sex = model.getValueAt(i,5).toString();
        switch(sex){
            case "M":
            Sex.setSelectedIndex(0);
            break;
            case "F":
            Sex.setSelectedIndex(1);
            break;
        }
        Salary.setText(model.getValueAt(i, 6).toString());
        DOB.setText(model.getValueAt(i, 7).toString());
        Role.setText(model.getValueAt(i, 8).toString());
        Address.setText(model.getValueAt(i, 9).toString());

    }//GEN-LAST:event_StaffMouseClicked

